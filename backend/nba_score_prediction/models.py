                if 1 in quarters:
                    self.trainable_models[1]['xgb_tuned'] = xgb.XGBRegressor(**params_q1)
                    self.log("Initialized trainable XGBoost model for Q1.")
                if 2 in quarters:
                     self.trainable_models[2]['xgb_tuned'] = xgb.XGBRegressor(**params_q2)
